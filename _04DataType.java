
public class _04DataType {
    public static void main(String[] args) {


        //整數
        byte smallNumber = 127; // 1 byte
        short mediumNumber = 32000; // 2 bytes
        int intNumber = 12345;//4 bytes
        long largeNumber = 9223372036854775807L; // 8 bytes

        //浮點數
        float pi = 3.14f; // 4 bytes
        double largeDecimal = 123456.789; // 8 bytes

        //////////////////////////////////////////////////////////////
        
        char letter = 'B'; // 2 bytes  //字元
        boolean isJavaFun = true; // 1 bit  //布林值
        String greeting = "Hello, Java!";  //字串

    }

}
