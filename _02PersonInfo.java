public class _02PersonInfo {
    
    public static void main(String[] args)
    {
        //會顯示成一行不換行 (輸出 : 姓名:小明年齡:20職業:學生)
        System.out.print("姓名:小明");
        System.out.print("年齡:20");
        System.out.print("職業:學生");

        System.out.println("");

        //會顯示成三行換行
        //姓名:小明
        //年齡:20
        //職業:學生
        System.out.println("姓名:小明");
        System.out.println("年齡:20");
        System.out.println("職業:學生");
    }
}
